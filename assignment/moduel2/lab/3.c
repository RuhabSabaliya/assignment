#include<stdio.h>
int main()
{
// this is a single line comment
/*This is a 
Multi line comment
*/
int a=20;
const int b=30;
float f=20.45;
char ch='r';
printf("The value of a is=%d",a);
printf("\nThe value of b is=%d",b);
printf("\nThe value of f is=%.2f",f);
printf("\nThe value of c is=%c",ch);
	return 0;
}

